#include <stdio.h>

int main (){


int a = 1;

    printf("WHILE LOOP\n");
    while (a <= 10){
        printf ("%d\n", a);
        a++;
     }
         printf("\n\n");  


int b;

    printf("FOR LOOP\n");
    for (b = 2; b <= 20; b += 2){
        printf("%d\n", b);
    }    
         printf("\n\n");   


int c = 1;

printf("DO-WHILE LOOP\n");
    do {
        printf("%d\n", c);
        c++;
    }    
    while (c <= 5);

    return 0;
}
